class TestWhatsAppWatcherErrorHandling(unittest.TestCase):
    """Test error handling and edge cases"""

    def test_empty_message_handling(self):
        """Test handling of empty messages"""
        messages = [
            "",  # Empty
            "   ",  # Whitespace only
            None,  # None
        ]

        keywords = ['urgent', 'asap']

        for message in messages:
            if message:
                message_lower = message.lower()
                has_keyword = any(kw in message_lower for kw in keywords)
                self.assertFalse(has_keyword)

    def test_special_character_handling(self):
        """Test messages with special characters"""
        messages = [
            "Urgent ⚠️ message",
            "ASAP 🔥 needed",
            "Invoice [attached]",
            "Payment & confirmation",
        ]

        keywords = ['urgent', 'asap', 'invoice', 'payment']

        for message in messages:
            message_lower = message.lower()
            has_keyword = any(kw in message_lower for kw in keywords)
            self.assertTrue(has_keyword)

    def test_case_insensitivity(self):
        """Test case-insensitive keyword matching"""
        keywords = ['urgent', 'asap']

        variants = [
            "URGENT",
            "Urgent",
            "urgeNt",
            "ASAP",
            "asap",
            "AsAp",
        ]

        for variant in variants:
            self.assertTrue(any(kw in variant.lower() for kw in keywords))


